void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
     

    if (huart->Instance == USART3)  // 你的 UART3
    {
        // 存入缓冲区
        if (rx_index < RX_BUFFER_SIZE - 1) {
            uart_rx_buffer[rx_index++] = rx_byte;

            // 如果收到 \n，就处理一整行
            if (rx_byte == '\n') {
                uart_rx_buffer[rx_index] = '\0';  // 添加字符串结尾

                float voltage, current;
                if (sscanf(uart_rx_buffer, "%f,%f", &voltage, &current) == 2) {
                    // ? 成功解析到两个 float
                    // 在这里处理电压电流值，例如打印/存储/显示
									
									temp_vol = voltage;
									temp_curr = current;
                }

                rx_index = 0;  // 重置索引，准备接收下一行
            }
        } else {
            rx_index = 0;  // 超过长度则丢弃
        }

        // 重新启动中断接收
        HAL_UART_Receive_IT(&huart3, &rx_byte, 1);
    }
		 
			
}

